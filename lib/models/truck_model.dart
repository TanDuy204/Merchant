class TruckModel {
  final String name;
  final String licensePlate;
  final String brand;
  final String code;
  final String type;
  final String status;
  final double loadCapacity;

  TruckModel({
    required this.name,
    required this.licensePlate,
    required this.brand,
    required this.code,
    required this.type,
    required this.status,
    required this.loadCapacity,
  });
}
